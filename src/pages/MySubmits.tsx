fetch(`${API_URL}/api/auth/users/${userEmail}/submits/?nocache=${new Date().getTime()}`);



/* –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞ MySubmits –∫–∞–∫ —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª—å–Ω–æ–≥–æ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞ —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞ –ø—Ä–æ–ø—Å–æ–≤ */
const MySubmits: React.FC<MySubmitsProps> = ({ darkMode, toggleTheme }) => {
    const [perevals, setPerevals] = useState<Pereval[]>([]); /* –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–ø–∏—Å–∫–∞ –ø–µ—Ä–µ–≤–∞–ª–æ–≤, –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –ø—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤ */
    const [errorMessage, setErrorMessage] = useState<string | null>(null); /* –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ, –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ null */

    useEffect(() => { /* –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ —Ö—É–∫–∞ useEffect –¥–ª—è –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö –ø—Ä–∏ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ */
        if (!userEmail) { /* –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è email –≤ localStorage */
            setErrorMessage("–û—à–∏–±–∫–∞: Email –Ω–µ –Ω–∞–π–¥–µ–Ω. –ê–≤—Ç–æ—Ä–∏–∑—É–π—Ç–µ—Å—å –∑–∞–Ω–æ–≤–æ."); /* –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ */
            return; /* –ü—Ä–µ—Ä—ã–≤–∞–Ω–∏–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è */
        }

        fetch(`${API_URL}${userEmail}/submits/?nocache=${new Date().getTime()}`) /* GET-–∑–∞–ø—Ä–æ—Å –∫ API —Å –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–º nocache –¥–ª—è –∏–∑–±–µ–∂–∞–Ω–∏—è –∫—ç—à–∏—Ä–æ–≤–∞–Ω–∏—è */
            .then(async response => {
                const data = await response.json(); /* –ü—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏–µ –æ—Ç–≤–µ—Ç–∞ –≤ JSON */
                console.log("üì• –û—Ç–≤–µ—Ç API:", data); /* –õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—Ç–≤–µ—Ç–∞ –¥–ª—è –æ—Ç–ª–∞–¥–∫–∏ */

                if (!response.ok) { /* –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å–ø–µ—à–Ω–æ—Å—Ç–∏ –æ—Ç–≤–µ—Ç–∞ */
                    throw new Error(data.message || "–û—à–∏–±–∫–∞ —Å–µ—Ä–≤–µ—Ä–∞"); /* –í—ã–±—Ä–æ—Å –æ—à–∏–±–∫–∏ —Å —Å–æ–æ–±—â–µ–Ω–∏–µ–º –æ—Ç —Å–µ—Ä–≤–µ—Ä–∞ */
                }

                if (!data || typeof data !== "object" || !Array.isArray(data.data)) { /* –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—Ç—Ä—É–∫—Ç—É—Ä—ã –¥–∞–Ω–Ω—ã—Ö */
                    setErrorMessage("–û—à–∏–±–∫–∞: –°–µ—Ä–≤–µ—Ä –≤–µ—Ä–Ω—É–ª –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ."); /* –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ */
                    return; /* –ü—Ä–µ—Ä—ã–≤–∞–Ω–∏–µ –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è */
                }
                setPerevals(data.data); /* –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–ø–∏—Å–∫–∞ –ø–µ—Ä–µ–≤–∞–ª–æ–≤ –≤ —Å–æ—Å—Ç–æ—è–Ω–∏–µ */
            })
            .catch(error => { /* –û–±—Ä–∞–±–æ—Ç–∫–∞ –æ—à–∏–±–æ–∫ –∑–∞–ø—Ä–æ—Å–∞ */
                console.error("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ –ø–µ—Ä–µ–≤–∞–ª–æ–≤:", error); /* –õ–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ –æ—à–∏–±–∫–∏ */
                setErrorMessage("–û—à–∏–±–∫–∞ —Å–µ—Ç–∏. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ."); /* –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ */
            });
    }, []); /* –ü—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π, —á—Ç–æ–±—ã —ç—Ñ—Ñ–µ–∫—Ç —Å—Ä–∞–±–æ—Ç–∞–ª —Ç–æ–ª—å–∫–æ –ø—Ä–∏ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ */

    return ( /* –í–æ–∑–≤—Ä–∞—Ç JSX —Å—Ç—Ä—É–∫—Ç—É—Ä—ã –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞ */
        <div className={`submit-container ${darkMode ? "dark-mode" : "light-mode"}`}> {/* –ì–ª–∞–≤–Ω—ã–π –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä —Å –¥–∏–Ω–∞–º–∏—á–µ—Å–∫–∏–º –∫–ª–∞—Å—Å–æ–º —Ç–µ–º—ã */}
            <h1 className="submit-title centered-title">–ú–æ–∏ –ø–µ—Ä–µ–≤–∞–ª—ã</h1> {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å –Ω–æ–≤—ã–º –∫–ª–∞—Å—Å–æ–º –¥–ª—è —Ü–µ–Ω—Ç—Ä–∏—Ä–æ–≤–∞–Ω–∏—è */}
            {errorMessage && ( /* –£—Å–ª–æ–≤–Ω—ã–π —Ä–µ–Ω–¥–µ—Ä–∏–Ω–≥ —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ */
                <p className="error-message">{errorMessage}</p> /* –¢–µ–∫—Å—Ç –æ—à–∏–±–∫–∏ —Å –∫–ª–∞—Å—Å–æ–º –¥–ª—è —Å—Ç–∏–ª–∏–∑–∞—Ü–∏–∏ */
            )}
            <div className="table-wrapper"> {/* –û–±—ë—Ä—Ç–∫–∞ —Ç–∞–±–ª–∏—Ü—ã –¥–ª—è –ø—Ä–æ–∫—Ä—É—Ç–∫–∏ –Ω–∞ –º–∞–ª–µ–Ω—å–∫–∏—Ö —ç–∫—Ä–∞–Ω–∞—Ö */}
                <table className="mysubmits-table"> {/* –¢–∞–±–ª–∏—Ü–∞ —Å –∫–ª–∞—Å—Å–æ–º –¥–ª—è —Å—Ç–∏–ª–∏–∑–∞—Ü–∏–∏ */}
                    <thead> {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Ç–∞–±–ª–∏—Ü—ã */}
                        <tr>
                            <th className="mysubmits-th">–ù–∞–∑–≤–∞–Ω–∏–µ</th> {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç–æ–ª–±—Ü–∞ "–ù–∞–∑–≤–∞–Ω–∏–µ" */}
                            <th className="mysubmits-th">–°—Ç–∞—Ç—É—Å</th> {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç–æ–ª–±—Ü–∞ "–°—Ç–∞—Ç—É—Å" */}
                            <th className="mysubmits-th">–î–µ–π—Å—Ç–≤–∏—è</th> {/* –ó–∞–≥–æ–ª–æ–≤–æ–∫ —Å—Ç–æ–ª–±—Ü–∞ "–î–µ–π—Å—Ç–≤–∏—è" */}
                        </tr>
                    </thead>
                    <tbody> {/* –¢–µ–ª–æ —Ç–∞–±–ª–∏—Ü—ã */}
                        {perevals.map((p) => ( /* –ò—Ç–µ—Ä–∞—Ü–∏—è –ø–æ —Å–ø–∏—Å–∫—É –ø–µ—Ä–µ–≤–∞–ª–æ–≤ */
                            <tr key={p.id} className="mysubmits-row"> {/* –°—Ç—Ä–æ–∫–∞ —Ç–∞–±–ª–∏—Ü—ã —Å —É–Ω–∏–∫–∞–ª—å–Ω—ã–º –∫–ª—é—á–æ–º –∏ –∫–ª–∞—Å—Å–æ–º */}
                                <td className="mysubmits-td">{p.title}</td> {/* –Ø—á–µ–π–∫–∞ —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º */}
                                <td className="mysubmits-td">{p.status === 1 ? "new" : "–û–±—Ä–∞–±–æ—Ç–∞–Ω"}</td> {/* –Ø—á–µ–π–∫–∞ —Å–æ —Å—Ç–∞—Ç—É—Å–æ–º */}
                                <td className="mysubmits-td"> {/* –Ø—á–µ–π–∫–∞ —Å –¥–µ–π—Å—Ç–≤–∏—è–º–∏ */}
                                    {p.status === 1 && ( /* –£—Å–ª–æ–≤–Ω—ã–π —Ä–µ–Ω–¥–µ—Ä–∏–Ω–≥ —Å—Å—ã–ª–∫–∏ –¥–ª—è —Å—Ç–∞—Ç—É—Å–∞ "new" */
                                        <Link to={`/edit/${p.id}`} className="edit-link">–†–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞—Ç—å</Link> /* –°—Å—ã–ª–∫–∞ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü—É —Ä–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞–Ω–∏—è */
                                    )}
                                </td>
                            </tr>
                        ))}
                    </tbody>
                </table>
            </div>
            <button onClick={toggleTheme} className="theme-btn centered-btn"> {/* –ö–Ω–æ–ø–∫–∞ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Ç–µ–º—ã */}
                {darkMode ? "–°–≤–µ—Ç–ª–∞—è —Ç–µ–º–∞" : "–¢—ë–º–Ω–∞—è —Ç–µ–º–∞"} {/* –£—Å–ª–æ–≤–Ω—ã–π —Ç–µ–∫—Å—Ç –∫–Ω–æ–ø–∫–∏ */}
            </button>
        </div>
    ); /* –ö–æ–Ω–µ—Ü –≤–æ–∑–≤—Ä–∞—â–∞–µ–º–æ–≥–æ JSX */
};

export default MySubmits; /* –≠–∫—Å–ø–æ—Ä—Ç –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞ MySubmits –∫–∞–∫ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ */