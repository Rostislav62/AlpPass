    const [perevals, setPerevals] = useState<Pereval[]>([]); // –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è —Å–ø–∏—Å–∫–∞ –ø–µ—Ä–µ–≤–∞–ª–æ–≤, –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ –ø—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤
    const [errorMessage, setErrorMessage] = useState<string | null>(null); // –°–æ—Å—Ç–æ—è–Ω–∏–µ –¥–ª—è —Å–æ–æ–±—â–µ–Ω–∏—è –æ–± –æ—à–∏–±–∫–µ, –∏–∑–Ω–∞—á–∞–ª—å–Ω–æ null
